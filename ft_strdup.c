#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		i;
	int		len_src;

	len_src = ft_strlen (src);
	dest = (char *) malloc (sizeof (char) * len_src + 1);
	if (dest == NULL)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
